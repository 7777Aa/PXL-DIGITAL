package ppt;

public class Opdracht15a {

	public static void main(String[] args) {
		// WHILE
		System.out.println("Oplossing met WHILE");
		int i = 2;
		int g = 1;
		while (i < 8) {
			System.out.println(g);
			i += 3;
		}

		// DO WHILE
		System.out.println("Oplossing met DO WHILE");
		int j = 2;
		int h = 1;
		do {
			System.out.println(h);
			j += 3;
		} while (j < 8);
	}

}
